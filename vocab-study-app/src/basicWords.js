export const BASIC_WORDS_FOR_GAOKAO_76 = new Set(BASIC_WORDS);

export function normalizeHeadword(word) {
  return String(word || '')
    .toLowerCase()
    .replace(/[’]/g, "'")
    .replace(/\s+/g, ' ')
    .trim();
}

export function isBasicForGaokao76(entry) {
  const word = normalizeHeadword(entry?.word);
  if (!word) return false;
  if (BASIC_WORDS_FOR_GAOKAO_76.has(word)) return true;

  const firstVariant = word.split(/[\/,;]/)[0]?.trim();
  if (BASIC_WORDS_FOR_GAOKAO_76.has(firstVariant)) return true;

  return false;
}
